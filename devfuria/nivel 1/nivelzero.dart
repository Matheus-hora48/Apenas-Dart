void main() {
  int somar(int num1, int num2) {
    return num1 + num2;
  }

  int subtrair(int num1, int num2) {
    return num1 - num2;
  }

  int multiplicar(int num1, int num2) {
    return num1 * num2;
  }

  double dividir(int num1, int num2) {
    return num1 / num2;
  }

  print(somar(8, 4));
  print(subtrair(8, 4));
  print(multiplicar(8, 4));
  print(dividir(8, 4));
}
